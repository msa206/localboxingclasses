// Group cities by first letter
export const citiesByLetter = allCities.reduce((acc, city) => {
  const firstLetter = city.name[0].toUpperCase()
  if (!acc[firstLetter]) {
    acc[firstLetter] = []
  }
  acc[firstLetter].push(city)
  return acc
}, {} as Record<string, typeof allCities>)

